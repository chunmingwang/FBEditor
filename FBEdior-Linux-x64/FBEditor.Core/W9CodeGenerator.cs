    private static string EscapeString(string? s)
    {
        if (s is null) return "";
        // FreeBASIC string escaping: double embedded quotes, drop CR, turn LF into
        // a literal `" + Chr(10) + "` so multi-line text stays valid in source.
        return s.Replace("\"", "\"\"").Replace("\r", "").Replace("\n", "\" + Chr(10) + \"");
    }

    private static string SanitizeVarName(string? s)
    {
        if (s is null) return "Unknown";
        var result = new StringBuilder();
        foreach (var c in s)
            if (char.IsLetterOrDigit(c))
                result.Append(c);
        if (result.Length == 0) return "Unknown";
        return result.ToString();
    }

    private static string SanitizeFilename(string? s)
    {
        if (s is null) return "untitled";
        var result = new StringBuilder();
        foreach (var c in s)
        {
            if (char.IsLetterOrDigit(c) || c == '_' || c == '-')
                result.Append(c);
            else if (c == ' ')
                result.Append('_');
        }
        if (result.Length == 0) return "untitled";
        return result.ToString().ToLower();
    }
