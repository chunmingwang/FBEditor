    private static void EmitGadgetCreation(StringBuilder sb, W9GadgetInstance g)
    {
        var tdef = W9GadgetRegistry.GetTypeDef(g.GadgetType);
        if (tdef is null) return;

        // Comment
        sb.AppendLine();
        sb.AppendLine("' --- " + tdef.DisplayName + ": " + g.EnumName + " ---");

        // Build the creation call based on gadget type
        switch (g.GadgetType)
        {
            case W9GadgetType.Button:
            {
                string style = string.IsNullOrEmpty(g.Style) ? "BS_DEFPUSHBUTTON" : g.Style;
                sb.AppendLine(tdef.W9FunctionName + "(" + g.EnumName + ", " +
                              g.X + ", " + g.Y + ", " + g.W + ", " + g.H + ", \"" +
                              EscapeString(g.Text) + "\", " + style + ")");
                break;
            }
            case W9GadgetType.TextLabel:
            {
                string style = string.IsNullOrEmpty(g.Style) ? "SS_NOTIFY" : g.Style;
                sb.AppendLine(tdef.W9FunctionName + "(" + g.EnumName + ", " +
                              g.X + ", " + g.Y + ", " + g.W + ", " + g.H + ", \"" +
                              EscapeString(g.Text) + "\", " + style + ")");
                break;
            }
            case W9GadgetType.Editor:
            {
                string edStyle = "ES_MULTILINE";
                if (g.HasVScroll) edStyle += " Or WS_VSCROLL";
                if (g.HasHScroll) edStyle += " Or WS_HSCROLL";
                sb.AppendLine(tdef.W9FunctionName + "(" + g.EnumName + ", " +
                              g.X + ", " + g.Y + ", " + g.W + ", " + g.H + ", \"" +
                              EscapeString(g.Text) + "\", " + edStyle + ")");
                if (g.IsReadOnly) sb.AppendLine("ReadOnlyEditor(" + g.EnumName + ", 1)");
                if (g.WordWrap) sb.AppendLine("SetTransferTextLineEditorGadget(" + g.EnumName + ", 1)");
                break;
            }
            case W9GadgetType.StringInput:
            {
                if (g.IsPassword)
                    sb.AppendLine(tdef.W9FunctionName + "(" + g.EnumName + ", " +
                                  g.X + ", " + g.Y + ", " + g.W + ", " + g.H + ", \"" +
                                  EscapeString(g.Text) + "\", ES_PASSWORD)");
                else
                    sb.AppendLine(tdef.W9FunctionName + "(" + g.EnumName + ", " +
                                  g.X + ", " + g.Y + ", " + g.W + ", " + g.H + ", \"" +
                                  EscapeString(g.Text) + "\")");
                break;
            }
            case W9GadgetType.CheckBox:
                sb.AppendLine(tdef.W9FunctionName + "(" + g.EnumName + ", " +
                              g.X + ", " + g.Y + ", " + g.W + ", " + g.H + ", \"" +
                              EscapeString(g.Text) + "\")");
                if (g.IsChecked) sb.AppendLine("SetGadgetState(" + g.EnumName + ", 1)");
                break;
            case W9GadgetType.OptionButton:
                sb.AppendLine(tdef.W9FunctionName + "(" + g.EnumName + ", " +
                              g.X + ", " + g.Y + ", " + g.W + ", " + g.H + ", \"" +
                              EscapeString(g.Text) + "\")");
                if (g.IsChecked) sb.AppendLine("SetGadgetState(" + g.EnumName + ", 1)");
                break;
            case W9GadgetType.ComboBox:
                sb.AppendLine(tdef.W9FunctionName + "(" + g.EnumName + ", " +
                              g.X + ", " + g.Y + ", " + g.W + ", " + g.H + ")");
                if (!string.IsNullOrEmpty(g.Items))
                    foreach (var item in g.Items.Split(LineSeparators, StringSplitOptions.RemoveEmptyEntries))
                        sb.AppendLine("AddComboBoxItem(" + g.EnumName + ", \"" + EscapeString(item.Trim()) + "\", -1)");
                break;
            case W9GadgetType.ListBox:
                sb.AppendLine(tdef.W9FunctionName + "(" + g.EnumName + ", " +
                              g.X + ", " + g.Y + ", " + g.W + ", " + g.H + ")");
                if (!string.IsNullOrEmpty(g.Items))
                    foreach (var item in g.Items.Split(LineSeparators, StringSplitOptions.RemoveEmptyEntries))
                        sb.AppendLine("AddListBoxItem(" + g.EnumName + ", \"" + EscapeString(item.Trim()) + "\")");
                break;
            case W9GadgetType.GroupBox:
                sb.AppendLine(tdef.W9FunctionName + "(" + g.EnumName + ", " +
                              g.X + ", " + g.Y + ", " + g.W + ", " + g.H + ", \"" +
                              EscapeString(g.Text) + "\")");
                break;
            case W9GadgetType.ImageBox:
                sb.AppendLine(tdef.W9FunctionName + "(" + g.EnumName + ", " +
                              g.X + ", " + g.Y + ", " + g.W + ", " + g.H + ")");
                if (!string.IsNullOrEmpty(g.ImagePath))
                {
                    sb.AppendLine("' TODO: Load image - Var hImg = Load_Image(\"" + EscapeString(g.ImagePath) + "\")");
                    sb.AppendLine("' SetImageGadget(" + g.EnumName + ", hImg)");
                }
                break;
            case W9GadgetType.ProgressBar:
                sb.AppendLine(tdef.W9FunctionName + "(" + g.EnumName + ", " +
                              g.X + ", " + g.Y + ", " + g.W + ", " + g.H + ", " +
                              g.MinValue + ", " + g.MaxValue + ")");
                if (g.CurrentValue > 0) sb.AppendLine("SetGadgetState(" + g.EnumName + ", " + g.CurrentValue + ")");
                break;
            case W9GadgetType.ScrollBar:
            {
                string style = g.Orientation == 1 ? "SB_VERT" : "SB_HORZ";
                sb.AppendLine(tdef.W9FunctionName + "(" + g.EnumName + ", " +
                              g.X + ", " + g.Y + ", " + g.W + ", " + g.H + ", " +
                              g.MinValue + ", " + g.MaxValue + ", " + style + ")");
                break;
            }
            case W9GadgetType.TrackBar:
                sb.AppendLine(tdef.W9FunctionName + "(" + g.EnumName + ", " +
                              g.X + ", " + g.Y + ", " + g.W + ", " + g.H + ", " +
                              g.MinValue + ", " + g.MaxValue + ")");
                if (g.CurrentValue > 0) sb.AppendLine("SetGadgetState(" + g.EnumName + ", " + g.CurrentValue + ")");
                break;
            case W9GadgetType.SpinBox:
                sb.AppendLine(tdef.W9FunctionName + "(" + g.EnumName + ", " +
                              g.X + ", " + g.Y + ", " + g.W + ", " + g.H + ", " +
                              g.MaxValue + ", " + g.MinValue + ", " + g.CurrentValue + ")");
                break;
            case W9GadgetType.TreeView:
            {
                string tvStyle = "";
                if (g.HasLines) tvStyle = "TVS_HASLINES Or TVS_LINESATROOT";
                if (g.HasButtons)
                    tvStyle = tvStyle == "" ? "TVS_HASBUTTONS" : tvStyle + " Or TVS_HASBUTTONS";
                if (g.HasCheckBoxes)
                    tvStyle = tvStyle == "" ? "TVS_CHECKBOXES" : tvStyle + " Or TVS_CHECKBOXES";
                if (tvStyle == "") tvStyle = "0";
                sb.AppendLine(tdef.W9FunctionName + "(" + g.EnumName + ", " +
                              g.X + ", " + g.Y + ", " + g.W + ", " + g.H + ", " +
                              tvStyle + ", WS_EX_CLIENTEDGE, 16)");
                break;
            }
            case W9GadgetType.ListView:
            {
                sb.AppendLine(tdef.W9FunctionName + "(" + g.EnumName + ", " +
                              g.X + ", " + g.Y + ", " + g.W + ", " + g.H + ")");
                for (int i = 0; i < g.ListViewColumns.Count; i++)
                    sb.AppendLine("AddListViewColumn(" + g.EnumName + ", \"" +
                                  EscapeString(g.ListViewColumns[i]) + "\", " + i + ", 120)");
                if (g.FullRowSelect || g.HasCheckBoxes)
                {
                    string exFlags = "";
                    if (g.FullRowSelect) exFlags = "LVS_EX_FULLROWSELECT";
                    if (g.HasCheckBoxes)
                        exFlags = exFlags == "" ? "LVS_EX_CHECKBOXES" : exFlags + " Or LVS_EX_CHECKBOXES";
                    sb.AppendLine("SetListViewExtendedStyle(" + g.EnumName + ", " + exFlags + ")");
                }
                break;
            }
            case W9GadgetType.StatusBar:
                sb.AppendLine(tdef.W9FunctionName + "(" + g.EnumName + ")");
                for (int i = 0; i < g.StatusBarFields.Count; i++)
                {
                    var sf = g.StatusBarFields[i];
                    sb.AppendLine("SetStatusBarField(" + g.EnumName + ", " + i + ", " +
                                  sf.Width + ", \"" + EscapeString(sf.Text) + "\")");
                }
                break;
            case W9GadgetType.PanelTab:
                sb.AppendLine(tdef.W9FunctionName + "(" + g.EnumName + ", " +
                              g.X + ", " + g.Y + ", " + g.W + ", " + g.H + ")");
                if (!string.IsNullOrEmpty(g.TabNames))
                {
                    var tabs = g.TabNames.Split(LineSeparators, StringSplitOptions.RemoveEmptyEntries);
                    for (int i = 0; i < tabs.Length; i++)
                        sb.AppendLine("Var hTab" + g.EnumName.Replace("gi", "") + i + " = AddPanelGadgetItem(" +
                                      g.EnumName + ", " + i + ", \"" + EscapeString(tabs[i].Trim()) + "\")");
                }
                break;
            case W9GadgetType.Container:
                sb.AppendLine(tdef.W9FunctionName + "(" + g.EnumName + ", " +
                              g.X + ", " + g.Y + ", " + g.W + ", " + g.H + ")");
                break;
            case W9GadgetType.Calendar:
                sb.AppendLine(tdef.W9FunctionName + "(" + g.EnumName + ", " +
                              g.X + ", " + g.Y + ", " + g.W + ", " + g.H + ")");
                break;
            case W9GadgetType.HyperLink:
                sb.AppendLine(tdef.W9FunctionName + "(" + g.EnumName + ", " +
                              g.X + ", " + g.Y + ", " + g.W + ", " + g.H + ", \"" +
                              EscapeString(g.Text) + "\")");
                break;
            default:
                sb.AppendLine("' Unsupported gadget type: " + g.GadgetType.ToString());
                break;
        }

        // Font — always set explicitly so gadgets have consistent appearance
        if (tdef.SupportsFont)
        {
            string fontName = string.IsNullOrEmpty(g.FontName) ? "Consolas" : g.FontName;
            int fontSize = g.FontSize > 0 ? g.FontSize : 11;
            sb.AppendLine("SetGadgetFont(" + g.EnumName + ", Cint(LoadFont(\"" + fontName + "\", " + fontSize + ")))");
        }

        // Colors
        if (g.BackColor != Color.Empty)
            sb.AppendLine("SetGadgetColor(" + g.EnumName + ", BGR(" +
                          g.BackColor.R + ", " + g.BackColor.G + ", " + g.BackColor.B + "), 0, 3)");
        if (g.ForeColor != Color.Empty)
            sb.AppendLine("SetGadgetColor(" + g.EnumName + ", BGR(" +
                          g.ForeColor.R + ", " + g.ForeColor.G + ", " + g.ForeColor.B + "), 1, 3)");

        // Tooltip
        if (!string.IsNullOrEmpty(g.Tooltip))
            sb.AppendLine("GadgetToolTip(" + g.EnumName + ", \"" + EscapeString(g.Tooltip) + "\")");

        // Enabled/Visible state
        if (!g.IsEnabled) sb.AppendLine("DisableGadget(" + g.EnumName + ", 1)");
        if (!g.IsVisible) sb.AppendLine("HideGadget(" + g.EnumName + ", 1)");
    }

    private static void EmitEventLoop(StringBuilder sb, W9FormDesign design)
    {
        sb.AppendLine("' ============================================================================");
        sb.AppendLine("' EVENT LOOP");
        sb.AppendLine("' ============================================================================");
        sb.AppendLine("Do");
        sb.AppendLine("    Select Case WaitEvent()");
        sb.AppendLine();

        // Resize event
        if (design.HandleResize && design.ProportionalResize)
        {
            sb.AppendLine("    Case EventSize");
            sb.AppendLine("        If EventHwnd = hMainForm Then");
            sb.AppendLine("            size_change(WindowWidth(hMainForm), WindowHeight(hMainForm))");
            sb.AppendLine("        End If");
            sb.AppendLine();
        }

        // Menu event
        if (design.MenuItems.Count > 0 || design.HasKeyboardShortcut)
        {
            sb.AppendLine("    Case EventMenu");
            sb.AppendLine("        Select Case EventNumber");
            if (design.HasKeyboardShortcut)
            {
                var defaultBtn = design.Gadgets.Find(g => g.ID == design.DefaultButtonID);
                if (defaultBtn != null)
                {
                    string handlerName = SanitizeVarName(defaultBtn.EnumName.Replace("gi", "On")) + "Click";
                    sb.AppendLine("        Case siDefKey");
                    sb.AppendLine("            " + handlerName + "()");
                }
                else
                {
                    sb.AppendLine("        Case siDefKey");
                    sb.AppendLine("            ' TODO: Handle Enter key");
                }
            }
            foreach (var topMenu in design.MenuItems)
                foreach (var child in topMenu.Children)
                    if (!child.IsSeparator)
                    {
                        sb.AppendLine("        Case " + child.EnumName);
                        sb.AppendLine("            ' TODO: Handle " + child.Text);
                    }
            sb.AppendLine("        End Select");
            sb.AppendLine();
        }

        // Timer event
        if (design.HasTimer)
        {
            sb.AppendLine("    Case EventTimer");
            sb.AppendLine("        ' TODO: Handle timer tick");
            sb.AppendLine();
        }

        // Close event
        sb.AppendLine("    Case EventClose");
        if (design.HasTimer)
            sb.AppendLine("        KillTimer(hMainForm, Timer1)");
        sb.AppendLine("        End");
        sb.AppendLine();

        // Gadget events (click, change, etc.)
        var clickGadgets = design.Gadgets.FindAll(g =>
            g.GadgetType == W9GadgetType.Button || g.OnClickEvent || g.OnChangeEvent || g.OnDoubleClickEvent);
        if (clickGadgets.Count > 0)
        {
            sb.AppendLine("    Case EventGadget");
            sb.AppendLine("        Select Case EventNumber");
            foreach (var g in clickGadgets)
            {
                string baseName = SanitizeVarName(g.EnumName.Replace("gi", ""));
                sb.AppendLine("        Case " + g.EnumName);

                if ((g.GadgetType == W9GadgetType.Button || g.OnClickEvent) && !g.OnChangeEvent && !g.OnDoubleClickEvent)
                    sb.AppendLine("            On" + baseName + "Click()");
                else if (g.OnClickEvent && g.OnChangeEvent)
                {
                    sb.AppendLine("            On" + baseName + "Click()");
                    sb.AppendLine("            On" + baseName + "Change()");
                }
                else if (g.OnChangeEvent && !g.OnClickEvent)
                    sb.AppendLine("            On" + baseName + "Change()");
                else if (g.GadgetType == W9GadgetType.Button)
                    sb.AppendLine("            On" + baseName + "Click()");

                if (g.OnDoubleClickEvent)
                {
                    sb.AppendLine("            ' Note: For double-click, check EventKey or use a timer");
                    sb.AppendLine("            ' On" + baseName + "DoubleClick()");
                }
            }
            sb.AppendLine("        End Select");
            sb.AppendLine();
        }

        sb.AppendLine("    End Select");
        sb.AppendLine("Loop");
    }

